def validate_no_schedule(text: str) -> None:
    forbidden = [
        "开发周期",
        "排期",
        "第 1 周",
        "第 2 周",
        "S1",
        "S2",
        "24 周",
        "上线时间",
        "2026/",
        "2026 年",
    ]
    hits = [word for word in forbidden if word in text]
    if hits:
        raise ValueError("Generated document contains schedule-like terms: " + ", ".join(hits))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--project-root", default=".", help="Project root path. Prefer '.' when the path contains non-ASCII characters.")
    parser.add_argument("--out-dir", default=None, help="Output directory")
    args = parser.parse_args()

    project_root = Path(args.project_root).resolve()
    out_dir = Path(args.out_dir).resolve() if args.out_dir else project_root / "deliverables" / "requirements"
    out_dir.mkdir(parents=True, exist_ok=True)

    ai_text = ai_doc(project_root)
    reader_text = reader_doc()
    validate_no_schedule(ai_text)
    validate_no_schedule(reader_text)

    ai_path = out_dir / "ai-functional-requirements.md"
    reader_path = out_dir / "reader-functional-requirements.md"
    ai_path.write_text(ai_text, encoding="utf-8", newline="\n")
    reader_path.write_text(reader_text, encoding="utf-8", newline="\n")

    print(ai_path)
    print(reader_path)


if __name__ == "__main__":
    main()
